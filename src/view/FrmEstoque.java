    private void btnAtualizaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizaActionPerformed
        // TODO add your handling code here:
        Produto p = new Produto();
        p.setNome(txtDescricao.getText());
        p.setPreco(Double.parseDouble(txtPreco.getText()));
        p.setQtde(Integer.parseInt(txtQtde.getText()));
        
        int aumentar = Integer.parseInt(spinnerInserir.getValue().toString());
        int remover = Integer.parseInt(spinnerRemover.getValue().toString());
        int qtdeestoque = p.getQtde() + aumentar - remover;
        
        lblDescricao.setText(p.getNome());
        lblPreco.setText(String.valueOf(p.getPreco()));
        lblQtde.setText(String.valueOf(p.getQtde() + aumentar - remover));
        lblTotal.setText(String.valueOf(p.getPreco() * qtdeestoque));
        
    }//GEN-LAST:event_btnAtualizaActionPerformed

